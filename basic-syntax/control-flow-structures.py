# Conditional Control Structure "if" in Python
x = 5

if x > 0:
    print("x is greater than zero.")
elif x == 0:
    print("x is equal to zero.")
else:
    print("x is less than zero.")

# Loop while in Python
y = 0

while y < 5:
    print(y)
    y += 1

# Loop for in Python
fruits = ["apple", "banana", "cherry"]

for fruit in fruits:
    print(fruit)