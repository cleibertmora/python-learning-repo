# Comparison Operators in Python
x = 5
y = 3

equality = x == y
inequality = x != y
greater_than = x > y
less_than = x < y
greater_than_or_equal_to = x >= y
less_than_or_equal_to = x <= y

# Print the results
print(equality)                 # Output: False
print(inequality)               # Output: True
print(greater_than)             # Output: True
print(less_than)                # Output: False
print(greater_than_or_equal_to) # Output: True
print(less_than_or_equal_to)    # Output: False
