# Arithmetic Operators in Python
x = 5
y = 3

sum = x + y
difference = x - y
product = x * y
division = x / y
integer_division = x // y
modulus = x % y
power = x ** y

# Print the results
print(sum)              # Output: 8
print(difference)       # Output: 2
print(product)          # Output: 15
print(division)         # Output: 1.6666666666666667
print(integer_division) # Output: 1
print(modulus)          # Output: 2
print(power)            # Output: 125