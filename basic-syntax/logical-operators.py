# Logical Operators in Python
x = 5
y = 3
z = 7

# "and" operator
print(x < y and x < z)   # Output: False

# "or" operator
print(x < y or x < z)    # Output: True

# "not" operator
print(not(x < y))        # Output: True

# The not() function is used to negate a boolean expression. 
# If the expression is true, not() returns False. 
# If the expression is false, not() returns True.